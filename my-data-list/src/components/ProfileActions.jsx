function ProfileActions({ onEditProfile, onThemeSelect }) {
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef(null);

  // Cierra el menú si se hace clic fuera de él
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setIsOpen(false);
      }
    };
    document.addEventListener("mousedown", handleClickOutside);
    return () => document.removeEventListener("mousedown", handleClickOutside);
  }, []);

  const handleActionClick = (action) => {
    action();
    setIsOpen(false); // Cierra el menú después de hacer clic en una opción
  };

  return (
    <div className="profile-actions-container" ref={dropdownRef}>
      <button className="settings-btn" onClick={() => setIsOpen(!isOpen)} title="Settings">
        <CogIcon />
      </button>

      {isOpen && (
        <div className="dropdown-menu">
          <button onClick={() => handleActionClick(onEditProfile)}>
            <EditIcon />
            <span>Editar Perfil</span>
          </button>
          <button onClick={() => handleActionClick(onThemeSelect)}>
            <ThemeIcon />
            <span>Cambiar Tema</span>
          </button>
          <hr />
          <button className="logout-btn" onClick={() => handleActionClick(logOut)}>
            <LogoutIcon />
            <span>Cerrar Sesión</span>
          </button>
        </div>
      )}
    </div>
  );
}

export default ProfileActions;
